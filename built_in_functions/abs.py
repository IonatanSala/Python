# abs(x)
# This will return the absolute value of a number
# x can be int or float
# if x is complex num, magnitude will be returned

my_negative_tuple = (-23, -7.3234, 4+3j)

print(abs(my_negative_tuple[0]))	# 23
print(abs(my_negative_tuple[1]))	# 7.3234
print(abs(my_negative_tuple[2]))	# 5.0 (the magnitude)